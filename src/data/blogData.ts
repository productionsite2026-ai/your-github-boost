export const getArticleBySlug = (slug: string) => {
  return blogArticles.find(article => article.slug === slug);
};

export const getArticlesByCategory = (category: string) => {
  if (category === "all") return blogArticles;
  return blogArticles.filter(article => 
    article.category.toLowerCase() === category.toLowerCase()
  );
};

export const getFeaturedArticle = () => {
  return blogArticles.find(article => article.featured);
};

export const getRelatedArticles = (currentSlug: string, limit: number = 3) => {
  const current = getArticleBySlug(currentSlug);
  if (!current) return [];
  
  return blogArticles
    .filter(article => 
      article.slug !== currentSlug && 
      (article.category === current.category || 
       article.tags.some(tag => current.tags.includes(tag)))
    )
    .slice(0, limit);
};
